import repro_project_dsl

package nimaddlib:
  uses:
    "nim >=2.2 <3.0"

  library nimaddlib

package rustapp:
  uses:
    "rust"

  executable rustapp:
    discard

# Manual dep edge: the Rust source's ``extern "C" { fn nimAdd(...); }``
# block references the Nim symbol by name; the Rust use-scanner does
# not walk extern blocks. The cross-language dep ``rustapp -> nimaddlib``
# is hand-authored here today.
depends_on rustapp: nimaddlib

# Workspace-internal dependencies, regenerated by `repro deps refresh`.
include "repro.scanned-deps.nim"
