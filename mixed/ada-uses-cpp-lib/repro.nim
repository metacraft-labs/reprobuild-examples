import repro_project_dsl

package mathlibPkg:
  uses:
    "gcc >=11"

  library mathlib

package adacalcPkg:
  uses:
    "gnatmake"

  executable adacalc:
    discard

# Manual dep edge: the Ada source's ``pragma Import (C, ...)`` declares
# the symbol but not the upstream package; the Ada scanner cannot
# infer the cross-language link from that evidence. Hand-authored
# here today; once a scanner extension learns to fold ``pragma Import``
# declarations into the dep graph, this edge moves into
# ``repro.scanned-deps.nim``.
depends_on adacalcPkg: mathlibPkg

# Workspace-internal dependencies, regenerated by `repro deps refresh`.
include "repro.scanned-deps.nim"
