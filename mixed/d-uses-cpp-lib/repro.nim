import repro_project_dsl

package mathlib:
  uses:
    "gcc >=11"

  library mathlib

package dcalc:
  uses:
    "d"

  executable dcalc:
    discard

# Manual dep edge: the D source's ``extern (C) c_add(...)`` declares
# the symbol but not the upstream package; the D scanner cannot
# infer the cross-language link from that evidence. Hand-authored
# here today; once a scanner extension learns to fold ``extern (C)``
# declarations into the dep graph, this edge moves into
# ``repro.scanned-deps.nim``.
depends_on dcalc: mathlib

# Workspace-internal dependencies, regenerated by `repro deps refresh`.
include "repro.scanned-deps.nim"
