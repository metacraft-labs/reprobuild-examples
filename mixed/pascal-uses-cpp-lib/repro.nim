import repro_project_dsl

package mathlibPkg:
  uses:
    "gcc >=11"

  library mathlib

package pascalcalcPkg:
  uses:
    "fpc"

  executable pascalcalc:
    discard

# Manual dep edge: the Pascal source's ``external name 'c_add'``
# declares the symbol but not the upstream package; the Pascal
# scanner cannot infer the cross-language link from that evidence.
# Hand-authored here today; once a scanner extension learns to fold
# ``external name`` declarations into the dep graph, this edge moves
# into ``repro.scanned-deps.nim``.
depends_on pascalcalcPkg: mathlibPkg

# Workspace-internal dependencies, regenerated by `repro deps refresh`.
include "repro.scanned-deps.nim"
