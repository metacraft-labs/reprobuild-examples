import repro_project_dsl

package mathlib:
  uses:
    "gcc >=11"

  library mathlib

package zigcalc:
  uses:
    "zig"

  executable zigcalc:
    discard

# Manual dep edge: the Zig source's ``extern fn c_add(...)`` declares
# the symbol but not the upstream package; the Zig scanner cannot
# infer the cross-language link from that evidence. Hand-authored
# here today; once a scanner extension learns to fold ``extern fn``
# declarations into the dep graph, this edge moves into
# ``repro.scanned-deps.nim``.
depends_on zigcalc: mathlib

# Workspace-internal dependencies, regenerated by `repro deps refresh`.
include "repro.scanned-deps.nim"
